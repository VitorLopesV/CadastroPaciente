package entities;

public class Patient {

// ATRIBUTOS
    /** Atributo responsável por receber o nome do paciente*/
    public String name;
    /** Atributo responsável por receber a idade do paciente*/
    public Integer age;
    /** Atributo responsável por receber a raça do paciente*/
    public String race;
    /** Atributo responsável por receber o peso do paciente*/
    public double weight;
    /** Atributo responsável por descobrir se o paciente é castrado ou não*/
    public String castrated;
    /** Atributo responsável por receber a infermidade do paciente */
    public String illness;





}
